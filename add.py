"""
add.py
-------
Provides a function to add two numbers.

Functions
---------
add(a, b)
    Returns the sum of a and b.
"""

def add(a, b):
    """Return the sum of a and b.
    
    Parameters
    ----------
    a : int or float
        First number to add.
    b : int or float
        Second number to add.
    
    Returns
    -------
    int or float
        The sum of a and b.
    """
    return a + b
