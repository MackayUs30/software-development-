"""
multiply.py
-----------
Provides a function to multiply two numbers.

Functions
---------
multiply(a, b)
    Returns the product of a and b.
"""

def multiply(a, b):
    """Return the product of a and b.
    
    Parameters
    ----------
    a : int or float
        First number to multiply.
    b : int or float
        Second number to multiply.
    
    Returns
    -------
    int or float
        The product of a and b.
    """
    return a * b
