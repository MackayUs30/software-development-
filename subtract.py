"""
subtract.py
-----------
Provides a function to subtract one number from another.

Functions
---------
subtract(a, b)
    Returns the result of subtracting b from a.
"""

def subtract(a, b):
    """Return the result of subtracting b from a.
    
    Parameters
    ----------
    a : int or float
        Number to subtract from.
    b : int or float
        Number to subtract.
    
    Returns
    -------
    int or float
        The result of a - b.
    """
    return a - b
